/*
    A loop in programming is a control structure used to repeatedly run or execute
    a specific block of code until a certain condition is met.

    The words iteration, go through, and looping are often used interchangeably to describe
    the process of executing a block of code multiple times.

    There are several types of loops in JavaScript, including:
    1. For Loop: Used when the number of iterations is known beforehand.
        It is used when the number of iterations or repetitions is known before hand.

    2. While Loop: Used when the number of iterations is not known and the loop needs
    to run until a certain condition is met.
    
    3. Do-While Loop: Similar to the while loop, but it guarantees that the block of code
    will be executed at least once, as the condition is evaluated after the block of code is executed.

    4. For...in Loop: Used to iterate over the properties of an object.

    5. For...of Loop: Used to iterate over iterable objects like arrays, strings, etc.

    6. forEach Loop: A method available on arrays that executes a provided function once for each array element.

    Loops are essential for performing repetitive tasks, such as iterating through arrays, processing data, or executing a block of code multiple times based on certain conditions.
*/